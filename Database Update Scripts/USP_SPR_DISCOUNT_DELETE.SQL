CREATE PROCEDURE [dbo].[USP_SPR_DISCOUNT_DELETE]  
(

 @DISCOUNTCODE VARCHAR(20),  
 @ID_ORDERTYPE VARCHAR(100),
 @SUPP_CURRENTNO VARCHAR(100),

 @RETVAL VARCHAR(20) OUTPUT
  
 )  
AS  
BEGIN  
IF EXISTS(SELECT * FROM TBL_SPR_DISCOUNTMATRIXBUY WHERE DISCOUNTCODE = @DISCOUNTCODE AND ID_ORDERTYPE = @ID_ORDERTYPE AND SUPP_CURRENTNO = @SUPP_CURRENTNO)
BEGIN
	DELETE FROM TBL_SPR_DISCOUNTMATRIXBUY
	WHERE DISCOUNTCODE = @DISCOUNTCODE AND ID_ORDERTYPE = @ID_ORDERTYPE AND SUPP_CURRENTNO = @SUPP_CURRENTNO
	SET @RETVAL = 'DELFLG'
	END
ELSE
BEGIN
	SET @RETVAL = 'ERROR'
END
END 